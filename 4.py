import re
"""Написать программу, которая читая символы из файла, распознает, преобразует и 
выводит на экран объекты по определенному правилу. Объекты разделены пробелами. 
Распознавание и преобразование делать по возможности через регулярные выражения. Для
упрощения под выводом числа прописью подразумевается последовательный вывод всех цифр числа.
Вещественные числа, заключенные в кавычки (все виды). Кавычки не выводятся. Целая часть числа 
выводится словами."""
d = {
    '0': 'ноль',
    '1': 'один',
    '2': 'два',
    '3': 'три',
    '4': 'четыре',
    '5': 'пять',
    '6': 'шесть',
    '7': 'семь',
    '8': 'восемь',
    '9': 'девять'
}

# Открытие файла
with open("3.txt", 'r') as f:
    s = f.readline()
    while s != '':
        # Разделение строки на объекты
        objects = s.split()
        for obj in objects:
            # Регулярное выражение для проверки числа в кавычках
            match = re.match(r"""^(['"])-?\d+[.,]\d+\1$""", obj.strip())
            if match:
                # Убираем кавычки
                obj = obj[1:-1]
                # Отделяем целую часть
                int_part = obj.split('.')[0].replace(',', '')  # Убираем запятую, если есть
                # Замена цифр на слова
                for key in d:
                    int_part = int_part.replace(key, d[key] + ' ')
                # Вывод преобразованного числа
                print(int_part.strip(), end='')
                print(obj[obj.index('.'):])  # Добавляем дробную часть
        s = f.readline()
